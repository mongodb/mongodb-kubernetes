"""Standalone cross-layer log analyzer.

Usage:
  python -m tests.common.search.log_analyzer.cli --namespace ls-25 --since 5m

Discovers MongoDBSearch + MongoDB resources in the namespace, pulls logs
from mongos / mongod / mongot / envoy LB pods over the given timeframe,
builds per-cursor cross-layer trees, and prints them. RS vs sharded is
auto-detected by inspecting ``spec.type`` on the MongoDB resource.

Examples:

  # Sharded: render last 5 minutes of operations from ns ls-25
  python -m tests.common.search.log_analyzer.cli \\
    --namespace ls-25 --mdbs-name mdb-sh-conn-tool --since 5m

  # RS: only print the timeline view, not the trees
  python -m tests.common.search.log_analyzer.cli \\
    --namespace ls-25 --since 10m --no-trees

  # Render the single-lsid interleaved timeline (color-coded per cursor).
  python -m tests.common.search.log_analyzer.cli \\
    --namespace ls-25 --since 5m --lsid e7e9aa0b... --color

The envoy access-log surface (always-on JSON stdout records emitted by
the operator-configured HCM) drives the ``envoy.stream`` tree branches.
For frame-level envoy debugging, POST to the admin endpoint manually:
``kubectl exec <envoy-pod> -- curl -X POST localhost:9901/logging?paths=...``.

This module does NOT inject pymongo CommandListener events (the live
test harness is the only source for those). Instead it synthesises
``ClientWireOp`` records from mongod COMMAND aggregate/getMore/
killCursors entries, so the existing ``build_cursor_trees`` /
``build_sharded_cursor_trees`` builders produce one tree per observed
cursor id.

Cross-layer correlation join keys
---------------------------------

Three keys carry across the layers. Spot-check them in the rendered tree:

* lsid (Logical Session ID — 32-hex-char UUID)
  Generated by pymongo, written by mongos and per-shard mongod in every
  COMMAND ``Slow query`` log record (``attr.command.lsid.id``). One lsid
  per logical session — every aggregate / getMore / killCursors on the
  same client cursor shares it. Load-bearing join: pytest <-> mongos <->
  per-shard mongod.

* cursor_id
  Allocated by mongod (or mongos for the top cursor) on the aggregate
  response. Mongos's ``attr.cursor.cursors[]`` lists each shard's
  sub-cursor id; per-shard mongod logs the same id on its own COMMAND
  records.

* clientId (UUID — 32-hex-char)
  Allocated by per-shard mongod when it opens a gRPC egress session to
  envoy. Surfaces in:
    - mongod NETWORK log id=7401401 ``attr.session.clientId``
    - envoy access/debug log ``mongodb-clientid`` request header
    - mongot ``MongoDbGrpcProtocolInterceptor`` DEBUG record
      ``attr.clientId``
  Load-bearing join: per-shard mongod <-> envoy <-> mongot. A single
  client cursor produces N clientIds for an N-way fanout (one per shard
  mongot connection).
"""

