def _synthesize_client_wire_ops(
    mongos_commands: list[MongosCommand],
    mongod_commands: list[MongodCommand],
    topology: str,
) -> list[ClientWireOp]:
    """Build fake pymongo wire-op records from server-side COMMAND logs."""
    ops: list[ClientWireOp] = []
    req_id = 1

    def _emit(command, ts, cursor_id, lsid):
        nonlocal req_id
        rid = req_id
        req_id += 1
        for phase in ("started", "succeeded"):
            ops.append(
                ClientWireOp(
                    phase=phase,
                    command_name=command,
                    request_id=rid,
                    timestamp=ts,
                    lsid=lsid,
                    cursor_id=cursor_id,
                )
            )

    if topology == "sharded":
        for mc in mongos_commands:
            if mc.timestamp is None or mc.cursor_id is None:
                continue
            _emit(mc.command, mc.timestamp, mc.cursor_id, mc.lsid)
    else:
        for mc in mongod_commands:
            if mc.timestamp is None or mc.cursor_id is None:
                continue
            if mc.command == "aggregate" and not mc.has_search_stage:
                continue
            _emit(mc.command, mc.timestamp, mc.cursor_id, mc.lsid)
    return ops


