def _pod_groups(
    namespace: str,
    mdb_name: str,
    mdbs_name: str,
    topology: str,
) -> dict[str, list[str]]:
    """Resolve which pods to pull logs from per layer.

    Keys: ``mongos``, ``mongod``, ``mongot``, ``envoy``. Empty lists when
    the layer doesn't apply.
    """
    lb_prefix = f"{mdbs_name}-search-lb-0-"
    envoy = log_collector.discover_pods(namespace, name_prefix=lb_prefix)

    if topology == "sharded":
        mongos = log_collector.discover_pods(namespace, name_prefix=f"{mdb_name}-mongos-")
        mongot = log_collector.discover_pods(namespace, name_prefix=f"{mdbs_name}-search-0-{mdb_name}-")

        # Shard mongods: filter to <mdb>-<idx>-<n> with idx in [0, shardCount).
        shard_count = _get_shard_count(namespace, mdb_name)
        shard_re = re.compile(rf"^{re.escape(mdb_name)}-(\d+)-\d+$")
        all_pods = log_collector.discover_pods(namespace, name_prefix=f"{mdb_name}-")
        mongod: list[str] = []
        for p in all_pods:
            m = shard_re.match(p)
            if m and (shard_count == 0 or int(m.group(1)) < shard_count):
                mongod.append(p)
        return {"mongos": mongos, "mongod": sorted(mongod), "mongot": mongot, "envoy": envoy}

    # RS topology
    rs_re = re.compile(rf"^{re.escape(mdb_name)}-\d+$")
    all_pods = log_collector.discover_pods(namespace, name_prefix=f"{mdb_name}-")
    mongod = sorted(p for p in all_pods if rs_re.match(p))
    # RS mongots live under `<search>-search-` but exclude envoy LB pods.
    raw_mongot = log_collector.discover_pods(namespace, name_prefix=f"{mdbs_name}-search-")
    mongot = sorted(p for p in raw_mongot if not p.startswith(lb_prefix))
    return {"mongos": [], "mongod": mongod, "mongot": mongot, "envoy": envoy}


