def _build_rs_trees(
    namespace: str,
    paths_by_group: dict[str, list[str]],
    collection_ns_regex: Optional[str] = None,
) -> tuple[list, list, "LogStore"]:
    """Return ``(trees, timeline_events, log_store)`` for the RS case.

    ``collection_ns_regex`` filters mongod_commands to those whose
    ``namespace`` (e.g. ``sample_mflix.movies``) matches the regex.
    """
    mongod_paths = paths_by_group.get("mongod", [])
    mongot_paths = paths_by_group.get("mongot", [])
    envoy_paths = paths_by_group.get("envoy", [])

    mongod_cmds = read_mongod_commands(mongod_paths, namespace=namespace) if mongod_paths else []
    if collection_ns_regex:
        pat = re.compile(collection_ns_regex)
        mongod_cmds = [c for c in mongod_cmds if c.namespace and pat.search(c.namespace)]
    mongod_sessions = read_mongod_sessions(mongod_paths, namespace=namespace) if mongod_paths else []
    envoy_streams = parse_envoy_debug_log(envoy_paths, namespace=namespace) if envoy_paths else []
    envoy_access = read_envoy_access_log(envoy_paths, namespace=namespace) if envoy_paths else []
    envoy_streams = merge_envoy_access_log_into_streams(envoy_streams, envoy_access)
    mongot_streams, mongot_batches = (
        build_stream_summaries(mongot_paths, namespace=namespace) if mongot_paths else ({}, [])
    )
    mongot_opens, mongot_cmds = (
        read_mongot_interceptor_events(mongot_paths, namespace=namespace) if mongot_paths else ([], [])
    )

    client_ops = parse_client_wire_ops(_synthesize_client_wire_ops([], mongod_cmds, "rs"))

    store = LogStore()
    store.load_from_parsed_records(
        client_ops=client_ops,
        mongod_commands=mongod_cmds,
        mongod_sessions=mongod_sessions,
        envoy_streams=envoy_streams,
        envoy_access=envoy_access,
        mongot_streams=mongot_streams,
        mongot_batches=mongot_batches,
        mongot_stream_opens=mongot_opens,
        mongot_cmds=mongot_cmds,
    )
    trees = build_cursor_trees_sql(store)
    timeline = build_unified_timeline_sql(store, include_client_ops=False)
    timeline.extend(_client_ops_as_timeline_events(client_ops))
    timeline.sort(key=lambda e: (e.timestamp, e.layer, e.pod or ""))
    return trees, timeline, store


def _client_ops_as_timeline_events(client_ops):
    """Surface synthesised ClientWireOp records as TimelineEvents."""
    from tests.common.search.log_analyzer.analyzer import TimelineEvent

    out = []
    seen = set()
    for op in client_ops:
        key = (op.request_id, op.phase)
        if key in seen or op.phase != "started":
            continue
        seen.add(key)
        out.append(
            TimelineEvent(
                timestamp=op.timestamp,
                layer="client",
                pod=None,
                kind=op.command_name,
                lsid=op.lsid,
                cursor_id=op.cursor_id,
                server_connection_id=op.server_connection_id,
            )
        )
    return out


def _build_sharded_trees(
    namespace: str,
    paths_by_group: dict[str, list[str]],
    mdb_name: str,
    mdbs_name: str,
    collection_ns_regex: Optional[str] = None,
) -> tuple[list, list, "LogStore"]:
    mongos_paths = paths_by_group.get("mongos", [])
    mongod_paths = paths_by_group.get("mongod", [])
    mongot_paths = paths_by_group.get("mongot", [])
    envoy_paths = paths_by_group.get("envoy", [])

    mongos_cmds = read_mongos_commands(mongos_paths, namespace=namespace) if mongos_paths else []
    mongos_reqs = read_mongos_remote_requests(mongos_paths, namespace=namespace) if mongos_paths else []
    mongod_cmds = read_mongod_commands(mongod_paths, namespace=namespace) if mongod_paths else []
    if collection_ns_regex:
        pat = re.compile(collection_ns_regex)
        mongos_cmds = [c for c in mongos_cmds if c.namespace and pat.search(c.namespace)]
        mongod_cmds = [c for c in mongod_cmds if c.namespace and pat.search(c.namespace)]
    mongod_sessions = read_mongod_sessions(mongod_paths, namespace=namespace) if mongod_paths else []
    envoy_streams = parse_envoy_debug_log(envoy_paths, namespace=namespace) if envoy_paths else []
    envoy_access = read_envoy_access_log(envoy_paths, namespace=namespace) if envoy_paths else []
    envoy_streams = merge_envoy_access_log_into_streams(envoy_streams, envoy_access)
    mongot_streams, mongot_batches = (
        build_stream_summaries(mongot_paths, namespace=namespace) if mongot_paths else ({}, [])
    )
    mongot_opens, mongot_cmds = (
        read_mongot_interceptor_events(mongot_paths, namespace=namespace) if mongot_paths else ([], [])
    )

    # Determine shard indices from observed mongod pod filenames.
    shard_indices: set[int] = set()
    pod_re = re.compile(rf"^{re.escape(mdb_name)}-(\d+)-\d+$")
    for path in mongod_paths:
        base = os.path.basename(path)
        if base.endswith(".log"):
            base = base[:-4]
        m = pod_re.match(base)
        if m:
            shard_indices.add(int(m.group(1)))
    shard_pod_prefixes = {f"{mdb_name}-{i}": f"{mdb_name}-{i}-" for i in sorted(shard_indices)}
    shard_mongot_pod_prefixes = {
        f"{mdb_name}-{i}": f"{mdbs_name}-search-0-{mdb_name}-{i}-" for i in sorted(shard_indices)
    }

    client_ops = parse_client_wire_ops(_synthesize_client_wire_ops(mongos_cmds, mongod_cmds, "sharded"))

    store = LogStore()
    store.load_from_parsed_records(
        client_ops=client_ops,
        mongod_commands=mongod_cmds,
        mongod_sessions=mongod_sessions,
        mongos_commands=mongos_cmds,
        mongos_remote_requests=mongos_reqs,
        envoy_streams=envoy_streams,
        envoy_access=envoy_access,
        mongot_streams=mongot_streams,
        mongot_batches=mongot_batches,
        mongot_stream_opens=mongot_opens,
        mongot_cmds=mongot_cmds,
    )
    trees = build_sharded_cursor_trees_sql(
        store,
        shard_pod_prefixes=shard_pod_prefixes,
        shard_mongot_pod_prefixes=shard_mongot_pod_prefixes,
    )

    timeline = build_unified_timeline_sql(store, include_client_ops=False)
    timeline.extend(_client_ops_as_timeline_events(client_ops))
    timeline.sort(key=lambda e: (e.timestamp, e.layer, e.pod or ""))
    return trees, timeline, store


