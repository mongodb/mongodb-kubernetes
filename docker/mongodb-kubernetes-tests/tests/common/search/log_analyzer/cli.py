def _resolve_paths_from_logs_dir(logs_dir: str) -> tuple[dict[str, list[str]], str]:
    """Discover per-layer log paths under a snapshot dir.

    Recognised subdirs (each may be missing or empty):
      ``mongod*/``, ``mongot*/`` (mongot-pre + mongot-post welcome),
      ``envoy*/``, ``mongos*/``. Returns ``({layer: [paths]}, topology)``;
      topology is "sharded" iff any mongos log file is present.
    """
    if not os.path.isdir(logs_dir):
        raise SystemExit(f"--logs-dir not a directory: {logs_dir}")

    def _collect(prefix: str) -> list[str]:
        return sorted(glob.glob(os.path.join(logs_dir, f"{prefix}*", "*.log")))

    paths = {
        "mongod": _collect("mongod"),
        "mongot": _collect("mongot"),
        "envoy": _collect("envoy"),
        "mongos": _collect("mongos"),
    }
    topology = "sharded" if paths["mongos"] else "rs"
    return paths, topology


def _parse_since_to_seconds(since: str) -> int:
    """``5m``/``90s``/``2h`` -> seconds (int)."""
    m = re.fullmatch(r"(\d+)([smh]?)", since.strip())
    if not m:
        raise SystemExit(f"invalid --since value {since!r} (use e.g. 30s, 5m, 1h)")
    n = int(m.group(1))
    unit = m.group(2) or "s"
    return n * {"s": 1, "m": 60, "h": 3600}[unit]


