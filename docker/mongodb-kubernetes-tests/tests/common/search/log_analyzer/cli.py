def main(argv: Optional[list[str]] = None) -> int:
    args = _parse_args(argv)

    if args.boost:
        return _run_boost(args)

    if args.logs_dir:
        paths_by_group, detected_topology = _resolve_paths_from_logs_dir(args.logs_dir)
        topology = args.topology or detected_topology
        namespace = args.namespace or ""
        mdb_name = args.mdb_name or ""
        mdbs_name = args.mdbs_name or ""
        print(f"# logs_dir={args.logs_dir}  topology={topology}  " f"mdb={mdb_name or '-'}  mdbs={mdbs_name or '-'}")
        for name, paths in paths_by_group.items():
            if paths:
                print(f"  {name}: {[os.path.basename(p) for p in paths]}")
    else:
        if not args.namespace:
            raise SystemExit("--namespace required (or use --logs-dir for offline replay)")
        _load_kube_config()
        namespace = args.namespace
        mdb_name, mdbs_name, spec_type = _resolve_mdb_and_mdbs(namespace, args.mdb_name, args.mdbs_name)
        topology = args.topology or _detect_topology(spec_type)
        print(
            f"# namespace={namespace}  mdb={mdb_name}  mdbs={mdbs_name}  "
            f"topology={topology}  spec.type={spec_type or '?'}"
        )
        groups = _pod_groups(namespace, mdb_name, mdbs_name, topology)
        since_seconds = _parse_since_to_seconds(args.since)
        paths_by_group = {}
        for name, pods in groups.items():
            if not pods:
                paths_by_group[name] = []
                continue
            print(f"  {name}: {pods}")
            paths_by_group[name] = log_collector.fetch_pod_logs(
                namespace,
                pods,
                since_seconds=since_seconds,
            )

    if topology == "sharded":
        if not (mdb_name and mdbs_name):
            raise SystemExit("sharded topology needs --mdb-name and --mdbs-name in --logs-dir mode")
        trees, timeline, store = _build_sharded_trees(
            namespace=namespace,
            paths_by_group=paths_by_group,
            mdb_name=mdb_name,
            mdbs_name=mdbs_name,
            collection_ns_regex=args.collection_namespace,
        )
    else:
        trees, timeline, store = _build_rs_trees(
            namespace=namespace,
            paths_by_group=paths_by_group,
            collection_ns_regex=args.collection_namespace,
        )

    if args.sql:
        cur = store.conn.execute(args.sql)
        cols = [d[0] for d in cur.description] if cur.description else []
        if cols:
            print("\t".join(cols))
        for row in cur:
            print("\t".join("" if v is None else str(v) for v in row))
        return 0

    if args.lsid:
        print_lsid_timeline(timeline, args.lsid, color=args.color)
    else:
        print_lsids_summary(timeline)
        if args.trees:
            if topology == "sharded":
                print_sharded_cursor_trees(trees)
            else:
                print_cursor_trees(trees)
        if args.timeline:
            print_unified_timeline(
                timeline,
                max_events=args.timeline_max,
                color=args.color,
            )

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
