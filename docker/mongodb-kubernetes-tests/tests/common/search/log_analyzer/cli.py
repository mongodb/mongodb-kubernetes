def _client_for_context(context: Optional[str] = None, *, set_default: bool = False) -> Optional[client.ApiClient]:
    """Build an ApiClient for one kube context, honoring per-cluster proxy-url.

    ``context=None`` selects the kubeconfig's current context. With
    ``set_default`` the resulting Configuration is installed as the process
    default and ``None`` is returned (so default-client callers — e.g. resource
    discovery — work); otherwise a dedicated ``ApiClient`` is returned, used by
    ``boost`` to fan out across the member clusters of a multi-cluster
    deployment (mongos / shard mongod / envoy pods live in different kind
    clusters). Falls back to in-cluster config when no kubeconfig is present.
    """
    try:
        from kubetester.kubetester import load_proxy_config

        merger = config.kube_config.KubeConfigMerger(config.kube_config.KUBE_CONFIG_DEFAULT_LOCATION)
        cfg = client.Configuration()
        config.load_kube_config_from_dict(merger.config, context=context, client_configuration=cfg)
        try:
            load_proxy_config(merger.config, cfg)
        except Exception:
            pass
        if set_default:
            client.Configuration.set_default(cfg)
            return None
        return client.ApiClient(configuration=cfg)
    except Exception:
        config.load_incluster_config()
        return None


def _load_kube_config() -> None:
    """Load the current-context kubeconfig (proxy-url aware) as the default."""
    _client_for_context(None, set_default=True)


_MDB_GROUP = "mongodb.com"
_MDB_VERSION = "v1"


def _list_mdb_resources(namespace: str, plural: str) -> list[dict]:
    api = client.CustomObjectsApi()
    try:
        res = api.list_namespaced_custom_object(
            group=_MDB_GROUP,
            version=_MDB_VERSION,
            namespace=namespace,
            plural=plural,
        )
    except client.exceptions.ApiException as exc:
        sys.stderr.write(f"# list {plural}/{namespace}: status={exc.status}\n")
        return []
    return res.get("items", []) or []


def _resolve_mdb_and_mdbs(
    namespace: str,
    mdb_name: Optional[str],
    mdbs_name: Optional[str],
) -> tuple[str, str, str]:
    """Return ``(mdb_name, mdbs_name, spec_type)``."""
    mdbs_items = _list_mdb_resources(namespace, "mongodbsearch")
    mdb_items = _list_mdb_resources(namespace, "mongodb")
    mdbs_list = [m["metadata"]["name"] for m in mdbs_items]
    mdb_list = [(m["metadata"]["name"], (m.get("spec") or {}).get("type", "")) for m in mdb_items]

    if mdbs_name is None and mdb_name is None:
        if len(mdbs_list) == 1:
            mdbs_name = mdbs_list[0]
        elif len(mdb_list) == 1:
            mdb_name = mdb_list[0][0]
        else:
            raise SystemExit(
                f"namespace {namespace!r} has {len(mdbs_list)} MongoDBSearch and "
                f"{len(mdb_list)} MongoDB resources; pass --mdbs-name and/or --mdb-name"
            )

    if mdb_name is None and mdbs_name is not None:
        candidates = [m for m, _ in mdb_list if m == mdbs_name]
        mdb_name = candidates[0] if candidates else mdbs_name

    if mdbs_name is None and mdb_name is not None:
        candidates = [m for m in mdbs_list if m == mdb_name]
        mdbs_name = candidates[0] if candidates else mdb_name

    spec_type = next((t for m, t in mdb_list if m == mdb_name), "")
    assert mdb_name is not None, "internal: mdb_name resolution invariant violated"
    assert mdbs_name is not None, "internal: mdbs_name resolution invariant violated"
    return mdb_name, mdbs_name, spec_type


def _detect_topology(spec_type: str) -> str:
    if spec_type.strip().lower() in {"shardedcluster", "sharded"}:
        return "sharded"
    return "rs"


def _get_shard_count(namespace: str, mdb_name: str) -> int:
    api = client.CustomObjectsApi()
    try:
        obj = api.get_namespaced_custom_object(
            group=_MDB_GROUP,
            version=_MDB_VERSION,
            namespace=namespace,
            plural="mongodb",
            name=mdb_name,
        )
    except client.exceptions.ApiException:
        return 0
    return int((obj.get("spec") or {}).get("shardCount") or 0)


